	CREATE VIEW [КОЛИЧЕСТВО ОБОРУДОВАНИЯ В ПОДРАЗДЕЛЕНИЯХ]
		AS SELECT Оборудование.Подразделение_Установки[ПОДРАЗДЕЛЕНИЕ],
				  COUNT(*)[КОЛИЧЕСТВО УСТАНОВОК ОБОРУДОВАНИЯ]
		FROM Оборудование
		GROUP BY Оборудование.Подразделение_Установки
GO
SELECT *FROM [КОЛИЧЕСТВО ОБОРУДОВАНИЯ В ПОДРАЗДЕЛЕНИЯХ]

