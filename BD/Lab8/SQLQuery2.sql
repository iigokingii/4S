	CREATE VIEW [ОТСОРТИРОВАННО](NAME,TYPE,DATE,NUMBER)
	AS SELECT TOP 150 Название_Оборудования,Тип_Оборудования,Дата_Поступления,Количество
	FROM Оборудование
	ORDER BY Название_Оборудования DESC
